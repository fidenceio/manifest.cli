# Template for README version update
generate_readme_version_section() {
    local version="$1"
    local timestamp="$2"

    # Check if we're in the Manifest CLI repo by looking for CLI-specific files
    local is_manifest_cli_repo=false
    if [[ -f "$PROJECT_ROOT/install-cli.sh" ]] && [[ -f "$PROJECT_ROOT/scripts/manifest-cli-wrapper.sh" ]] && [[ -d "$PROJECT_ROOT/modules" ]]; then
        is_manifest_cli_repo=true
    fi

    cat << EOF
## ðŸ“‹ Version Information

| Property | Value |
|----------|-------|
| **Current Version** | \`${version}\` |
| **Release Date** | \`${timestamp}\` |
| **Git Tag** | \`v${version}\` |
| **Branch** | \`main\` |
| **Last Updated** | \`${timestamp}\` |
| **CLI Version** | \`${version}\` |
EOF

    # Only include Documentation Files section for the Manifest CLI repo itself
    if [[ "$is_manifest_cli_repo" == true ]]; then
        cat << EOF

### ðŸ“š Documentation Files

- **Version Info**: [VERSION](VERSION)
- **CLI Modules**: [modules/](modules/)
- **Install Script**: [install-cli.sh](install-cli.sh)
EOF
    else
        # For non-Manifest CLI repos, add a footer with link
        cat << EOF

---
*This repo is versioned and documented by [Manifest CLI](https://github.com/fidenceio/manifest.cli).*
EOF
    fi
}

