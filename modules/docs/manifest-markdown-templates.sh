# Main function for command-line usage
main() {
    case "${1:-help}" in
        "release")
            generate_release_notes_template "${2:-}" "${3:-}" "${4:-}"
            ;;
        "changelog")
            generate_changelog_template "${2:-}" "${3:-}" "${4:-}"
