#!/bin/bash

# Manifest Markdown Templates Module
# Provides consistent, well-formatted markdown generation and templates

# Markdown templates module - uses PROJECT_ROOT from core module

# Markdown formatting functions
markdown_header() {
    local level="$1"
    local title="$2"
    local emoji="$3"
    
    # Ensure proper heading level (1-6)
    if [ "$level" -lt 1 ]; then level=1; fi
    if [ "$level" -gt 6 ]; then level=6; fi
    
    local hashes=$(printf "%*s" "$level" | tr ' ' '#')
    echo "${hashes} ${emoji}${title}"
}

markdown_section() {
    local title="$1"
    local emoji="$2"
    echo ""
    echo "## ${emoji}${title}"
    echo ""
}

markdown_subsection() {
    local title="$1"
    local emoji="$2"
    echo ""
    echo "### ${emoji}${title}"
    echo ""
}

markdown_list_item() {
    local text="$1"
    local emoji="$2"
    echo "- ${emoji}${text}"
}

markdown_code_block() {
    local language="$1"
    local code="$2"
    echo ""
    echo "\`\`\`${language}"
    echo "$code"
    echo "\`\`\`"
    echo ""
}

markdown_inline_code() {
    local code="$1"
    echo "\`${code}\`"
}

markdown_bold() {
    local text="$1"
    echo "**${text}**"
}

markdown_italic() {
    local text="$1"
    echo "*${text}*"
}

markdown_link() {
    local text="$1"
    local url="$2"
    echo "[${text}](${url})"
}

markdown_table_header() {
    local headers=("$@")
    local header_line=""
    local separator_line=""
    
    for header in "${headers[@]}"; do
        header_line="${header_line}| ${header} "
        separator_line="${separator_line}|---"
    done
    
    echo "${header_line}|"
    echo "${separator_line}|"
}

markdown_table_row() {
    local values=("$@")
    local row=""
    
    for value in "${values[@]}"; do
        row="${row}| ${value} "
    done
    
    echo "${row}|"
}

