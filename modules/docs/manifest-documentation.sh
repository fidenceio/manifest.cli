# Update README version information
# Preserves user-crafted README content. Only updates inline version strings
# and the version metadata section if one already exists. Never prepends a
# new metadata block to a README that doesn't have one.
update_readme_version() {
    local version="$1"
    local timestamp="$2"

    log_info "Updating README version information..."

    local readme_file="$PROJECT_ROOT/README.md"

    if [[ ! -f "$readme_file" ]]; then
        log_warning "README.md not found, skipping version update"
        return 0
    fi

    if manifest_is_canonical_repo "$PROJECT_ROOT"; then
        local temp_file
        temp_file=$(mktemp)

        if grep -q "## 📋 Version Information" "$readme_file"; then
            # README has a version metadata section — replace it in place
            local version_section
            version_section=$(generate_readme_version_section "$version" "$timestamp")
            local version_section_file
            version_section_file=$(mktemp)
            echo "$version_section" > "$version_section_file"

            local start_line end_line
            start_line=$(grep -n "## 📋 Version Information" "$readme_file" | head -1 | cut -d: -f1)
            end_line=$(tail -n +$((start_line + 1)) "$readme_file" | grep -n "^## " | head -1 | cut -d: -f1)

            if [[ -n "$end_line" ]]; then
                end_line=$((start_line + end_line - 1))
            else
                end_line=$(wc -l < "$readme_file")
            fi

            if [[ "$start_line" -gt 1 ]]; then
                head -n $((start_line - 1)) "$readme_file" > "$temp_file"
            else
                : > "$temp_file"
            fi
            cat "$version_section_file" >> "$temp_file"
            tail -n +$((end_line + 1)) "$readme_file" >> "$temp_file"

            rm -f "$version_section_file"
            mv "$temp_file" "$readme_file"
        else
            rm -f "$temp_file"
            local old_version=""
            if [[ -f "$PROJECT_ROOT/VERSION" ]]; then
                old_version=$(grep -oE '`[0-9]+\.[0-9]+\.[0-9]+`' "$readme_file" | head -1 | tr -d '`')
            fi
            if [[ -n "$old_version" ]] && [[ "$old_version" != "$version" ]]; then
                sed -i'' -e "s|\`${old_version}\`|\`${version}\`|g" "$readme_file"
                sed -i'' -e "s|RELEASE_v[0-9][0-9.]*\.md|RELEASE_v${version}.md|g" "$readme_file"
                rm -f "${readme_file}-e"
                log_debug "Updated inline version references: $old_version -> $version"
            fi
        fi

        log_success "README version information updated"
        return 0
    fi

    local version_section_file
    version_section_file=$(mktemp)
    generate_readme_version_section "$version" "$timestamp" > "$version_section_file"

    if manifest_is_legacy_generated_readme "$readme_file"; then
        create_default_readme "$readme_file"
    fi

    if manifest_file_has_managed_block "$readme_file" "$MANIFEST_README_VERSION_START" "$MANIFEST_README_VERSION_END"; then
        manifest_replace_managed_block "$readme_file" "$MANIFEST_README_VERSION_START" "$MANIFEST_README_VERSION_END" "$version_section_file"
    else
        log_info "Preserving existing README formatting; no Manifest-managed version block found."
    fi

    rm -f "$version_section_file"

    log_success "README version information updated"
}

# Generate documentation index
# If an INDEX.md already exists, updates version numbers and release note
# links in place, preserving user-crafted structure and formatting.
# Only creates the file from a template when it doesn't exist at all.
generate_docs_index() {
    local version="$1"

    log_info "Generating documentation index..."

    local index_file="$(get_docs_dir)/INDEX.md"

    if manifest_is_canonical_repo "$PROJECT_ROOT"; then
        if [[ -f "$index_file" ]]; then
            local old_version=""
            old_version=$(grep -oE '`[0-9]+\.[0-9]+\.[0-9]+`' "$index_file" | head -1 | tr -d '`')
            if [[ -n "$old_version" ]] && [[ "$old_version" != "$version" ]]; then
                sed -i'' -e "s|\`${old_version}\`|\`${version}\`|g" "$index_file"
            fi

            sed -i'' -e "s|^\*\*Version:\*\* [0-9][0-9.]*|\*\*Version:\*\* $version|" "$index_file"
            sed -i'' -e "s|RELEASE_v[0-9][0-9.]*\.md|RELEASE_v${version}.md|g" "$index_file"
            sed -i'' -e "s|CHANGELOG_v[0-9][0-9.]*\.md|CHANGELOG_v${version}.md|g" "$index_file"
            sed -i'' -e "s|Notes v[0-9][0-9.]*|Notes v${version}|g" "$index_file"
            sed -i'' -e "s|Changelog v[0-9][0-9.]*|Changelog v${version}|g" "$index_file"

            local today
            today=$(date -u '+%Y-%m-%d')
            sed -i'' -e "s|^\*\*Updated:\*\* [0-9-]*|\*\*Updated:\*\* $today|" "$index_file"
            rm -f "${index_file}-e"

            log_success "Documentation index updated: $index_file"
            return 0
        fi

        cat > "$index_file" << EOF
# Manifest CLI Documentation

**Version:** $version | **Updated:** $(date -u '+%Y-%m-%d')

---

## Getting Started

| Document | Description |
| -------- | ----------- |
| [Installation Guide](INSTALLATION.md) | Setup, upgrade, uninstall, and troubleshooting |
| [User Guide](USER_GUIDE.md) | Workflows, daily commands, and configuration |
| [Examples](EXAMPLES.md) | Real-world workflow recipes |

## Reference

| Document | Description |
| -------- | ----------- |
| [Command Reference](COMMAND_REFERENCE.md) | Every command, flag, and option |
| [Git Hooks](GIT_HOOKS.md) | Pre-commit secret protection and hook management |

## Current Release

| Document | Description |
| -------- | ----------- |
| [Release Notes v${version}](RELEASE_v${version}.md) | What's new in this release |
| [Changelog v${version}](CHANGELOG_v${version}.md) | Detailed change log |
| [Archived Releases](zArchive/) | Previous version documentation |

---

## Quick Start

\`\`\`bash
# Install
brew tap fidenceio/tap && brew install manifest

# Prepare a release locally
manifest prep patch

# Publish a release
manifest ship minor

# Get help
manifest --help
\`\`\`

<!-- Manifest CLI v$version -->
EOF

        log_success "Documentation index generated: $index_file"
        return 0
    fi

    if [[ -f "$index_file" ]]; then
        if manifest_is_legacy_generated_index "$index_file"; then
            write_external_docs_index "$index_file" "$version"
        elif manifest_file_has_managed_block "$index_file" "$MANIFEST_INDEX_METADATA_START" "$MANIFEST_INDEX_METADATA_END" && manifest_file_has_managed_block "$index_file" "$MANIFEST_INDEX_CURRENT_RELEASE_START" "$MANIFEST_INDEX_CURRENT_RELEASE_END"; then
            local metadata_file current_release_file
            metadata_file=$(mktemp)
            current_release_file=$(mktemp)
            generate_external_index_metadata_block "$version" > "$metadata_file"
            generate_external_index_current_release_block "$version" > "$current_release_file"
            manifest_replace_managed_block "$index_file" "$MANIFEST_INDEX_METADATA_START" "$MANIFEST_INDEX_METADATA_END" "$metadata_file"
            manifest_replace_managed_block "$index_file" "$MANIFEST_INDEX_CURRENT_RELEASE_START" "$MANIFEST_INDEX_CURRENT_RELEASE_END" "$current_release_file"
            rm -f "$metadata_file" "$current_release_file"
        else
            log_info "Preserving existing documentation index formatting; no Manifest-managed blocks found."
            return 0
        fi

        log_success "Documentation index updated: $index_file"
        return 0
    fi

    write_external_docs_index "$index_file" "$version"
    log_success "Documentation index generated: $index_file"
}

update_root_changelog_for_release() {
    local version="$1"
    local timestamp="$2"
    local root_changelog="$PROJECT_ROOT/CHANGELOG.md"
    local latest_changelog="$(get_docs_folder "$PROJECT_ROOT")/CHANGELOG_v$version.md"

    if manifest_is_canonical_repo "$PROJECT_ROOT"; then
        if [[ -f "$root_changelog" ]] && ! manifest_is_legacy_generated_root_changelog "$root_changelog" && ! manifest_file_has_managed_block "$root_changelog" "$MANIFEST_ROOT_CHANGELOG_START" "$MANIFEST_ROOT_CHANGELOG_END"; then
            log_info "Preserving existing root CHANGELOG.md; no Manifest-managed block found."
            return 0
        fi

        if [[ -f "$latest_changelog" ]]; then
            cp "$latest_changelog" "$root_changelog"
            log_success "Root CHANGELOG.md updated from latest canonical changelog"
        else
            log_warning "Latest changelog not found: $latest_changelog"
        fi
        return 0
    fi

    if [[ ! -f "$root_changelog" ]] || manifest_is_legacy_generated_root_changelog "$root_changelog"; then
        create_default_changelog "$root_changelog"
    fi

    if manifest_file_has_managed_block "$root_changelog" "$MANIFEST_ROOT_CHANGELOG_START" "$MANIFEST_ROOT_CHANGELOG_END"; then
        local changelog_block_file
        changelog_block_file=$(mktemp)
        generate_root_changelog_release_block "$version" "$timestamp" > "$changelog_block_file"
        manifest_replace_managed_block "$root_changelog" "$MANIFEST_ROOT_CHANGELOG_START" "$MANIFEST_ROOT_CHANGELOG_END" "$changelog_block_file"
        rm -f "$changelog_block_file"
        log_success "Root CHANGELOG.md updated"
        return 0
    fi

    log_info "Preserving existing root CHANGELOG.md formatting; no Manifest-managed block found."
}

