#!/bin/bash

# Manifest Documentation Module
# Orchestrates document generation using atomized modules

# Documentation module - uses MANIFEST_CLI_PROJECT_ROOT from core module

# Get configurable documentation directory
get_docs_dir() {
    get_docs_folder "$MANIFEST_CLI_PROJECT_ROOT"
}

# Import required modules
MANIFEST_CLI_SCRIPT_DIR="$(get_script_dir)"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/git/manifest-git-changes.sh"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/docs/manifest-markdown-templates.sh"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/docs/manifest-markdown-validation.sh"
# manifest_make_scratch_path lives here — every temp-file site below depends
# on it. Idempotent thanks to the module's load guard.
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/system/manifest-install-paths.sh"

MANIFEST_CLI_README_VERSION_START="<!-- manifest:readme-version:start -->"
MANIFEST_CLI_README_VERSION_END="<!-- manifest:readme-version:end -->"
MANIFEST_CLI_ROOT_CHANGELOG_START="<!-- manifest:root-changelog:start -->"
MANIFEST_CLI_ROOT_CHANGELOG_END="<!-- manifest:root-changelog:end -->"
MANIFEST_CLI_INDEX_METADATA_START="<!-- manifest:index-metadata:start -->"
MANIFEST_CLI_INDEX_METADATA_END="<!-- manifest:index-metadata:end -->"
MANIFEST_CLI_INDEX_CURRENT_RELEASE_START="<!-- manifest:index-current-release:start -->"
MANIFEST_CLI_INDEX_CURRENT_RELEASE_END="<!-- manifest:index-current-release:end -->"
MANIFEST_CLI_DOCS_SITE_MANAGED_MARKER="Managed by Manifest CLI docs site generation"

manifest_file_has_managed_block() {
    local file="$1"
    local start_marker="$2"
    local end_marker="$3"

    [[ -f "$file" ]] || return 1
    grep -Fqx "$start_marker" "$file" && grep -Fqx "$end_marker" "$file"
}

manifest_replace_managed_block() {
    local file="$1"
    local start_marker="$2"
    local end_marker="$3"
    local content_file="$4"
    local start_line end_line temp_file

    start_line=$(grep -nFx "$start_marker" "$file" | head -1 | cut -d: -f1)
    end_line=$(grep -nFx "$end_marker" "$file" | head -1 | cut -d: -f1)

    if [[ -z "$start_line" ]] || [[ -z "$end_line" ]] || (( end_line <= start_line )); then
        return 1
    fi

    temp_file=$(mktemp "$(manifest_make_scratch_path docs)/tmp.XXXXXXXX")
    if (( start_line > 1 )); then
        head -n $((start_line - 1)) "$file" > "$temp_file"
    else
        : > "$temp_file"
    fi

    printf '%s\n' "$start_marker" >> "$temp_file"
    cat "$content_file" >> "$temp_file"
    printf '%s\n' "$end_marker" >> "$temp_file"
    tail -n +$((end_line + 1)) "$file" >> "$temp_file"
    mv "$temp_file" "$file"
}

manifest_is_legacy_generated_readme() {
    local readme_file="$1"
    [[ -f "$readme_file" ]] || return 1

    if grep -Fq "This project uses [Manifest CLI]" "$readme_file"; then
        return 0
    fi
    if grep -Fq "This repo is versioned and documented by [Manifest CLI]" "$readme_file"; then
        return 0
    fi
    if grep -Fq "| **CLI Version** |" "$readme_file"; then
        return 0
    fi

    return 1
}

manifest_is_legacy_generated_root_changelog() {
    local changelog_file="$1"
    [[ -f "$changelog_file" ]] || return 1
    grep -Fq "Generated by Manifest CLI v" "$changelog_file"
}

manifest_is_legacy_generated_index() {
    local index_file="$1"
    [[ -f "$index_file" ]] || return 1
    grep -Fq "<!-- Manifest CLI v" "$index_file" || grep -Fq "# Manifest CLI Documentation" "$index_file"
}

generate_external_index_metadata_block() {
    local version="$1"
    cat << EOF
**Version:** $version | **Updated:** $(date -u '+%Y-%m-%d')
EOF
}

generate_external_index_current_release_block() {
    local version="$1"

    cat << EOF
| Document | Description |
| -------- | ----------- |
| [CHANGELOG.md](../CHANGELOG.md) | All releases, newest first |
EOF
}

write_external_docs_index() {
    local index_file="$1"
    local version="$2"
    local repo_name
    repo_name="$(manifest_repo_display_name "$MANIFEST_CLI_PROJECT_ROOT")"
    local metadata_file current_release_file
    metadata_file=$(mktemp "$(manifest_make_scratch_path docs)/tmp.XXXXXXXX")
    current_release_file=$(mktemp "$(manifest_make_scratch_path docs)/tmp.XXXXXXXX")

    generate_external_index_metadata_block "$version" > "$metadata_file"
    generate_external_index_current_release_block "$version" > "$current_release_file"

    cat > "$index_file" << EOF
# ${repo_name} Documentation

${MANIFEST_CLI_INDEX_METADATA_START}
$(cat "$metadata_file")
${MANIFEST_CLI_INDEX_METADATA_END}

## Current Release
${MANIFEST_CLI_INDEX_CURRENT_RELEASE_START}
$(cat "$current_release_file")
${MANIFEST_CLI_INDEX_CURRENT_RELEASE_END}
EOF

    rm -f "$metadata_file" "$current_release_file"
}

_manifest_docs_config_bool() {
    local value="$1"
    local default="${2:-true}"
    if [[ -z "$value" ]]; then
        value="$default"
    fi
    is_truthy "$value"
}

_manifest_docs_site_enabled() {
    _manifest_docs_config_bool "${MANIFEST_CLI_DOCS_GENERATE_SITE:-false}" "false" || \
        _manifest_docs_config_bool "${MANIFEST_CLI_DOCS_SITE_ENABLED:-false}" "false"
}

_manifest_docs_site_workflow_enabled() {
    _manifest_docs_config_bool "${MANIFEST_CLI_DOCS_GENERATE_SITE_WORKFLOW:-true}" "true"
}

_manifest_docs_write_managed_file() {
    local file="$1"
    local marker="$2"
    local content_file="$3"

    if [[ -f "$file" ]] && ! grep -Fq "$marker" "$file"; then
        log_error "Refusing to overwrite unmanaged docs site file: $file"
        return 1
    fi

    mkdir -p "$(dirname "$file")"
    cp "$content_file" "$file"
}

_manifest_docs_repo_title() {
    local project_root="${1:-$MANIFEST_CLI_PROJECT_ROOT}"
    local configured="${MANIFEST_CLI_DOCS_SITE_TITLE:-}"
    if [[ -n "$configured" ]]; then
        printf '%s\n' "$configured"
        return 0
    fi
    manifest_repo_display_name "$project_root"
}

