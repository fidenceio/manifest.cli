# Manifest Documentation Module
# Orchestrates document generation using atomized modules

# Documentation module - uses PROJECT_ROOT from core module

# Get configurable documentation directory
get_docs_dir() {
    get_docs_folder "$PROJECT_ROOT"
}

# Import required modules
MANIFEST_CLI_SCRIPT_DIR="$(get_script_dir)"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/git/manifest-git-changes.sh"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/docs/manifest-markdown-templates.sh"
source "$(dirname "$MANIFEST_CLI_SCRIPT_DIR")/docs/manifest-markdown-validation.sh"

MANIFEST_README_VERSION_START="<!-- manifest:readme-version:start -->"
MANIFEST_README_VERSION_END="<!-- manifest:readme-version:end -->"
MANIFEST_ROOT_CHANGELOG_START="<!-- manifest:root-changelog:start -->"
MANIFEST_ROOT_CHANGELOG_END="<!-- manifest:root-changelog:end -->"
MANIFEST_INDEX_METADATA_START="<!-- manifest:index-metadata:start -->"
MANIFEST_INDEX_METADATA_END="<!-- manifest:index-metadata:end -->"
MANIFEST_INDEX_CURRENT_RELEASE_START="<!-- manifest:index-current-release:start -->"
MANIFEST_INDEX_CURRENT_RELEASE_END="<!-- manifest:index-current-release:end -->"

manifest_file_has_managed_block() {
    local file="$1"
    local start_marker="$2"
    local end_marker="$3"

    [[ -f "$file" ]] || return 1
    grep -Fqx "$start_marker" "$file" && grep -Fqx "$end_marker" "$file"
}

manifest_replace_managed_block() {
    local file="$1"
    local start_marker="$2"
    local end_marker="$3"
    local content_file="$4"
    local start_line end_line temp_file

    start_line=$(grep -nFx "$start_marker" "$file" | head -1 | cut -d: -f1)
    end_line=$(grep -nFx "$end_marker" "$file" | head -1 | cut -d: -f1)

    if [[ -z "$start_line" ]] || [[ -z "$end_line" ]] || (( end_line <= start_line )); then
        return 1
    fi

    temp_file=$(mktemp)
    if (( start_line > 1 )); then
        head -n $((start_line - 1)) "$file" > "$temp_file"
    else
        : > "$temp_file"
    fi

    printf '%s\n' "$start_marker" >> "$temp_file"
    cat "$content_file" >> "$temp_file"
    printf '%s\n' "$end_marker" >> "$temp_file"
    tail -n +$((end_line + 1)) "$file" >> "$temp_file"
    mv "$temp_file" "$file"
}

manifest_replace_markdown_section() {
    local file="$1"
    local heading="$2"
    local content_file="$3"
    local start_line end_line temp_file

    start_line=$(grep -nF "$heading" "$file" | head -1 | cut -d: -f1)
    [[ -n "$start_line" ]] || return 1

    end_line=$(tail -n +$((start_line + 1)) "$file" | grep -n "^## " | head -1 | cut -d: -f1)
    if [[ -n "$end_line" ]]; then
        end_line=$((start_line + end_line - 1))
    else
        end_line=$(wc -l < "$file")
    fi

    temp_file=$(mktemp)
    if (( start_line > 1 )); then
        head -n $((start_line - 1)) "$file" > "$temp_file"
    else
        : > "$temp_file"
    fi
    cat "$content_file" >> "$temp_file"
    tail -n +$((end_line + 1)) "$file" >> "$temp_file"
    mv "$temp_file" "$file"
}

manifest_is_legacy_generated_readme() {
    local readme_file="$1"
    [[ -f "$readme_file" ]] || return 1

    if grep -Fq "This project uses [Manifest CLI]" "$readme_file"; then
        return 0
    fi
    if grep -Fq "This repo is versioned and documented by [Manifest CLI]" "$readme_file"; then
        return 0
    fi
    if grep -Fq "| **CLI Version** |" "$readme_file"; then
        return 0
    fi

    return 1
}

manifest_is_legacy_generated_root_changelog() {
    local changelog_file="$1"
    [[ -f "$changelog_file" ]] || return 1
    grep -Fq "Generated by Manifest CLI v" "$changelog_file"
}

manifest_is_legacy_generated_index() {
    local index_file="$1"
    [[ -f "$index_file" ]] || return 1
    grep -Fq "<!-- Manifest CLI v" "$index_file" || grep -Fq "# Manifest CLI Documentation" "$index_file"
}

generate_root_changelog_release_block() {
    local version="$1"
    local timestamp="$2"
    local release_date="${timestamp%% *}"
    local docs_dir_name
    docs_dir_name="$(basename "$(get_docs_folder "$PROJECT_ROOT")")"

    cat << EOF
## [${version}] - ${release_date}

See [${docs_dir_name}/CHANGELOG_v${version}.md](${docs_dir_name}/CHANGELOG_v${version}.md) for the detailed changelog and [${docs_dir_name}/RELEASE_v${version}.md](${docs_dir_name}/RELEASE_v${version}.md) for release notes.
EOF
}

generate_external_index_metadata_block() {
    local version="$1"
    cat << EOF
**Version:** $version | **Updated:** $(date -u '+%Y-%m-%d')
EOF
}

generate_external_index_current_release_block() {
    local version="$1"
    local archive_dir_name
    archive_dir_name="$(basename "$(get_zarchive_dir)")"

    cat << EOF
| Document | Description |
| -------- | ----------- |
| [Release Notes v${version}](RELEASE_v${version}.md) | Summary for this release |
| [Changelog v${version}](CHANGELOG_v${version}.md) | Detailed change log |
| [Archived Releases](${archive_dir_name}/) | Previous version documentation |
EOF
}

write_external_docs_index() {
    local index_file="$1"
    local version="$2"
    local repo_name
    repo_name="$(manifest_repo_display_name "$PROJECT_ROOT")"
    local metadata_file current_release_file
    metadata_file=$(mktemp)
    current_release_file=$(mktemp)

    generate_external_index_metadata_block "$version" > "$metadata_file"
    generate_external_index_current_release_block "$version" > "$current_release_file"

    cat > "$index_file" << EOF
# ${repo_name} Documentation

${MANIFEST_INDEX_METADATA_START}
$(cat "$metadata_file")
${MANIFEST_INDEX_METADATA_END}

## Current Release
${MANIFEST_INDEX_CURRENT_RELEASE_START}
$(cat "$current_release_file")
${MANIFEST_INDEX_CURRENT_RELEASE_END}
EOF

    rm -f "$metadata_file" "$current_release_file"
}

# Generate release notes
generate_release_notes() {
    local version="$1"
    local timestamp="$2"
    local release_type="$3"
    local changes_file="$4"
    
    log_info "Generating release notes for v$version..."
    
    local release_file="$(get_docs_dir)/RELEASE_v$version.md"
    
    # Generate template
    local content=$(generate_release_notes_template "$version" "$timestamp" "$release_type")
    
    # Add actual changes if available
    if [[ -f "$changes_file" ]]; then
        local changes=$(cat "$changes_file" | sed 's/\x1b\[[0-9;]*m//g')  # Remove color codes
        # Append changes to the end of the document
        content="${content}

## 🔧 Actual Changes

${changes}"
    fi
    
    # Write the file with proper newline
    echo "$content" > "$release_file"
    echo "" >> "$release_file"
    
    if [[ -f "$release_file" ]]; then
        log_success "Release notes generated: $release_file"
        return 0
    else
        log_error "Failed to generate release notes"
        return 1
    fi
}

