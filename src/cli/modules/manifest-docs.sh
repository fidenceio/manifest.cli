update_repository_metadata() {
    echo "ğŸ·ï¸  Updating repository metadata..."
    
    # Detect repository provider
    local remote_url=$(git remote get-url origin 2>/dev/null)
    local provider=""
    
    if [[ "$remote_url" =~ github\.com ]]; then
        provider="github"
    elif [[ "$remote_url" =~ gitlab\.com ]]; then
        provider="gitlab"
    else
        echo "   âš ï¸  Unknown repository provider, skipping metadata update"
        return 0
    fi
    
    echo "   ğŸ”„ Automatically updating repository metadata..."
    
    case "$provider" in
        "github")
            update_github_metadata
            ;;
        "gitlab")
            update_gitlab_metadata
            ;;
        *)
            echo "   âš ï¸  Unsupported provider: $provider"
            return 1
            ;;
    esac
}

update_github_metadata() {
    echo "   âœ… Detected provider: github"
    
    # Check if GitHub CLI is available
    if ! command -v gh &> /dev/null; then
        echo "   âš ï¸  GitHub CLI (gh) not found, skipping metadata update"
        return 0
    fi
    
    # Check if authenticated
    if ! gh auth status &> /dev/null; then
        echo "   âš ï¸  GitHub CLI not authenticated, skipping metadata update"
        return 0
    fi
    
    # Get repository name
    local repo_name=$(basename -s .git "$(git remote get-url origin)")
    local org_name=$(git remote get-url origin | sed -n 's/.*github\.com[:/]\([^/]*\)\/.*/\1/p')
    
    if [ -z "$org_name" ] || [ -z "$repo_name" ]; then
        echo "   âš ï¸  Could not determine repository details, skipping metadata update"
        return 0
    fi
    
    local full_repo="$org_name/$repo_name"
    
    # Update description
    if gh repo edit "$full_repo" --description "Manifest CLI - A powerful command-line tool for Git operations and version management" 2>/dev/null; then
        echo "   âœ… Description updated"
    else
        echo "   âš ï¸  Description update failed (common due to API limitations)"
    fi
    
    # Update topics
    if gh repo edit "$full_repo" --add-topic "cli,git,versioning,automation,bash,shell" 2>/dev/null; then
        echo "   âœ… Topics updated"
    else
        echo "   âš ï¸  Topics update failed (common due to API limitations)"
    fi
    
    # Update homepage
    if gh repo edit "$full_repo" --homepage "https://github.com/$full_repo#readme" 2>/dev/null; then
        echo "   âœ… Homepage updated"
    else
        echo "   âš ï¸  Homepage update failed"
    fi
    
    echo "   âœ… Repository metadata updated automatically"
}

